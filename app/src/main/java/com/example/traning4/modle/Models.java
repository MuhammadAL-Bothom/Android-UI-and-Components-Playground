package com.example.traning4.modle;

public class Models {
    private final Integer iconid;
    private final String name;

    public Models(Integer iconid, String name) {
        this.iconid = iconid;
        this.name = name;
    }

    public Integer getIconid() {
        return iconid;
    }

    public String getName() {
        return name;
    }
}
